// ●	●	●	-	○	○	○
// 游戏的规则是：一次只能移动一个棋子；棋子可以向空格中移动，也可以跳过一个对方的格子进入空格，但不能向后跳，也不能跳过两个子。请用计算机实现上述游戏。

