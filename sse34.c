#include <stdio.h>
#define N 15
void jiaohuan(char a[],int dex[]);
void printHanshu(char a[]);
int num=0;
int main()
{
    char star[N]={'|','*','|','*','|','*','|',' ','|','@','|','@','|','@','|'}; 
    //竖线表示格子也不管
    int Index[16]={7,5,9,11,7,3,1,5,9,13,11,7,3,5,9,7};
    //这里是16不是15,若是15则最后一次没有换(9 7没有互换)                                                                            
    jiaohuan(star,Index);
    return 0;
}
void jiaohuan(char a[],int dex[])
{
    char temp;
    int i;
    printHanshu(a);
    for(i=0;i<=14;i++)
    {
        temp = a[dex[i]];
        a[dex[i]] = a[dex[i+1]];
        a[dex[i+1]] = temp;
        printHanshu(a);                      //先交换再打印
    }
}
void printHanshu(char a[])
{
    int i;
    printf("No.%2d:-----------\n",num);
    printf("      ");
    for(i=0;i<15;i++)
    {
        printf("%c",a[i]);
    }
    printf("\n    -----------\n");
    num++;
}
